<?php

namespace App\Mail;

use App\Models\User;
use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class RegisterMail extends Mailable
{
    use Queueable, SerializesModels;

    private $user;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $actionLink = "email/verify/{$this->user->uuid}";
        return $this->subject('Bem-vindo ao Organize PDF! Confirme sua conta')->with(['actionLink' => $actionLink])->view('mails.actions.welcome');
    }
}
